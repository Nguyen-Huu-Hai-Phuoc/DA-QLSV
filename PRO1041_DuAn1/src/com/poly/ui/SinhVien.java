    public void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblSinhVien.getModel();
        model.setRowCount(0);
        try {
            lop loP = (lop) cboLop.getSelectedItem();
            if (loP != null) {
                List<sinhvien> list = dao.selectByLop(loP.getMaLop());

                for (int i = 0; i < list.size(); i++) {
                    sinhvien sinhVien = list.get(i);
                    Object[] row = {
                        sinhVien.getMaSV(),
                        sinhVien.getHoTen(),
                        XDate.toString(sinhVien.getNgaySinh()),
                        sinhVien.isGioiTinh() ? "Nam" : "Nữ",
                        sinhVien.getDiaChi(),
                        sinhVien.getMaLop(),
                        sinhVien.getHinh(),
                        sinhVien.getEmail()
                    };

                    model.addRow(row);
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
            DialogHelPer.alert(this, "Lỗi truy cập dữ liệu!");
        }
    }

    @SuppressWarnings("unchecked")
    public void fillComboBoxKhoa() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboKhoa.getModel();
        model.removeAllElements();
        List<khoa> list = kdao.selectAll();
        for (khoa kh : list) {
            model.addElement(kh);
        }
        this.fillComboBoxLop();
    }

    @SuppressWarnings("unchecked")
    public void fillComboBoxLop() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboLop.getModel();
        model.removeAllElements();
        khoa kh = (khoa) cboKhoa.getSelectedItem();
        if (kh != null) {
            List<lop> list = ldao.selectByKhoaHoc(kh.getMaKhoa());
            for (lop lp : list) {
                model.addElement(lp);
            }
            this.fillTable();
        }
    }

