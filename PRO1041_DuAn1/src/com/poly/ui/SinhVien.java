    sinhvien getForm() {
        sinhvien model = new sinhvien();
        model.setMaSV(txtMaSV.getText());
        model.setHoTen(txtHovaTen.getText());
        model.setNgaySinh(XDate.toDate(txtNgaySinh.getText()));
        model.setGioiTinh(rdoNam.isSelected());
        model.setDiaChi(txtDiaChi.getText());
        model.setMaLop(cboMaLop.getSelectedItem().toString());
//        model.setMaLop(txtMaLop.getText());
        model.setHinh(lblHinh.getToolTipText());
        model.setEmail(txtEmail.getText());
        return model;
    }

    public void setForm(sinhvien model) {
        txtMaSV.setText(model.getMaSV());
        txtHovaTen.setText(model.getHoTen());
        txtNgaySinh.setText(XDate.toString(model.getNgaySinh()));
        rdoNam.setSelected(model.isGioiTinh());
        rdoNu.setSelected(!model.isGioiTinh());
        txtDiaChi.setText(model.getDiaChi());
        lblHinh.setToolTipText(model.getHinh());
        if (model.getHinh() != null) {
            ImageIcon icon = XImage.read(model.getHinh());
            lblHinh.setIcon(new ImageIcon(icon.getImage().getScaledInstance(158, 157, Image.SCALE_DEFAULT)));
        }
//        txtMaLop.setText(model.getMaLop());
        cboMaLop.setSelectedItem(model.getMaLop());
        txtEmail.setText(model.getEmail());
    }

    public void updateStatus() {
        boolean edit = this.index >= 0;
        boolean first = this.index == 0;
        boolean last = this.index == tblSinhVien.getRowCount() - 1;
        txtMaSV.setEditable(!edit);
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);

        btnFirst.setEnabled(edit && !first);
        btnprev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }

    public void clearForm() {
        sinhvien sv = new sinhvien();
        this.setForm(sv);
        this.index = -1;
        txtNgaySinh.setText("");
//        fillComboBoxMaLop();
        ImgDefault();
        this.updateStatus();
    }

