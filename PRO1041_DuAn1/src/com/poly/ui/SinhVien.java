    public void edit() {
        try {
            String maSV = (String) tblSinhVien.getValueAt(this.index, 0);
            sinhvien model = dao.findById(maSV);
            if (model != null) {
                setForm(model);
                updateStatus();
                jTabbedPane1.setSelectedIndex(0);

            }
        } catch (Exception e) {
            DialogHelPer.alert(this, "Lỗi truy cập dữ liệu!");
            e.printStackTrace();
        }
    }

